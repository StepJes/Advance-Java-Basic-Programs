package strings;

public class Concat {
	public static void main(String[] args) {
		int a = 10;
		int b = 20;
		int ab= a+b;

		System.out.println("a int and b int: " + ab);

		String c = "10";
		int d = 20;
		String cd = c+d;

		System.out.println("c int and d int: " + cd);

		int e = 10;
		String f = "20";
		String ef = e+f;
		

		System.out.println("e int and f int: " + ef);

		String g = "10";
		String h = "20";
		String gh = g+h;

		System.out.println("g int and h int: " + gh);
	}
}
